    void setStatusVitri(){
        boolean first = this.vitri <= 0;
        boolean last = this.vitri >= lstCD.size() - 1;
        btn_back.setEnabled(!first);
        btn_first.setEnabled(!first);
        btn_last.setEnabled(!last);
        btn_next.setEnabled(!last);
    }
    
    void edit(){
//        String manv = (String) tbl_Table.getValueAt(this.vitri, 0);
        for (ChuyenDe cd : lstCD) {
            if (this.txtMaCD.getText().equals(cd.getMaCD())) {
                this.btn_them.setEnabled(false);
                this.btn_update.setEnabled(true);
                this.btn_xoa.setEnabled(true);
                break;
            }else{
                this.btn_them.setEnabled(true);
                this.btn_update.setEnabled(false);
                this.btn_xoa.setEnabled(false);
                continue;
            }
        }
    }
    
    void setStatus(boolean insertable){
        this.btn_them.setEnabled(insertable);
        this.btn_update.setEnabled(!insertable);
        this.btn_xoa.setEnabled(!insertable);
        this.btn_last.setEnabled(true);
        this.btn_back.setEnabled(true);
        this.btn_first.setEnabled(true);
        this.btn_next.setEnabled(true);
    }
    
    private void clear(){
        this.txtMaCD.setText("");
        this.TxtTenCD.setText("");
        this.txtHocPhi.setText("");
        this.txtMoTa.setText("");
        this.txtThoiLuong.setText("");
        this.lbl_anh.setText("");
        ImageIcon image1 = new ImageIcon();
        this.lbl_anh.setIcon(image1);
        this.setStatus(true);
    }
    private boolean check(){
        if (this.TxtTenCD.getText().trim().equals("") || this.txtMaCD.getText().trim().equals("") ||
            this.txtMoTa.getText().trim().equals("") ||this.txtHocPhi.getText().trim().equals("") ||
                this.txtThoiLuong.getText().trim().equals("")||this.lbl_tenanh.getText().trim().equals("")    
                ) {
            JOptionPane.showMessageDialog(this, "Chưa nhập đầy đủ thông tin!");
            return false;
        }
        return true;
    }
