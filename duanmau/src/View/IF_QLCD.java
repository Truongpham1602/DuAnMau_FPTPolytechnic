    private void lbl_anhMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lbl_anhMouseClicked
        JFileChooser fc = new JFileChooser("src\\Image");
        int r = fc.showOpenDialog(this);
        if (r == JFileChooser.APPROVE_OPTION) {
            try {
                File file = fc.getSelectedFile(); //lấy file người dùng chọn
                String anh = file.getAbsolutePath();
                ImageIcon icon2 = new ImageIcon(anh);
                Image im = icon2.getImage();
                ImageIcon icon = new ImageIcon(im.getScaledInstance(this.lbl_anh.getWidth(), this.lbl_anh.getHeight(), im.SCALE_SMOOTH));
                // Lấy được tên hình ảnh    
                this.lbl_tenanh.setText(""+fc.getSelectedFile().getName());
                this.lbl_anh.setIcon(icon);
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_lbl_anhMouseClicked

    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed
        try {
            for (ChuyenDe cd : lstCD) {
                System.out.println(cd.getMaCD());
                System.out.println(this.txtMaCD.getText());
                if (this.txtMaCD.getText().equals(cd.getMaCD())) {
                    int xn = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa Mã chuyên đề: "+this.txtMaCD.getText(),"Thông Báo", JOptionPane.YES_NO_OPTION);
                    if (xn == 0){
                        this.CDDAO.delete(this.txtMaCD.getText());
                        this.getAll();
                        this.clear();
                        JOptionPane.showMessageDialog(this, "Xóa thành công!");
                    }
                    return;
                }else{
                    continue;
                }
            }
            JOptionPane.showMessageDialog(this, "Mã nhân viên bạn cần xóa không tồn tại!");
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btn_xoaActionPerformed

    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        if (this.check()) {
            try {
                for (ChuyenDe cd : lstCD) {
                    if (this.txtMaCD.getText().equals(cd.getMaCD())) {
                        String macd = this.txtMaCD.getText();
                        int xn = JOptionPane.showConfirmDialog(this, "Bạn có muốn sửa Mã Chuyên Đề: "+macd,"Thông Báo", JOptionPane.YES_NO_OPTION);
                        if (xn == 0){
                            String ten = this.TxtTenCD.getText();
                            float hocphi = Float.parseFloat(this.txtHocPhi.getText());;
                            int thoiluong = Integer.parseInt(this.txtThoiLuong.getText());
                            String mota = this.txtMoTa.getText();
                            String tenanh = this.lbl_tenanh.getText();
                            
                            cd = new ChuyenDe(macd, ten, hocphi, thoiluong, tenanh, mota);
                            this.CDDAO.update(cd);
                            JOptionPane.showMessageDialog(this, "Sửa thành công!");
                            this.getAll();
                            this.clear();
                        }
                        return;
                    }else{
                        continue;
                    }
                }
                JOptionPane.showMessageDialog(this, "Mã nhân viên bạn cần sửa không tồn tại!");
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btn_updateActionPerformed

    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
         if (this.check()) {
            try {
                for (ChuyenDe cd : lstCD) {
                    if (this.txtMaCD.getText().equals(cd.getMaCD())) {
                        JOptionPane.showMessageDialog(this, "Mã chuyên đề này đã tồn tại!");
                        return;
                    }
                }
                    String macd = this.txtMaCD.getText();
                    String ten = this.TxtTenCD.getText();
                    double hocphi = Double.parseDouble(this.txtHocPhi.getText());;
                    int thoiluong = Integer.parseInt(this.txtThoiLuong.getText());
                    String mota = this.txtMoTa.getText();
                    String tenanh = this.lbl_tenanh.getText();
                    ChuyenDe cd = new ChuyenDe(macd, ten, hocphi, thoiluong, tenanh, mota);
                    this.CDDAO.insert(cd);
                
                    JOptionPane.showMessageDialog(this, "Thêm thành công!");
                    this.getAll();
                    this.clear();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btn_themActionPerformed
    private void showAgain(){
        this.vitri = this.tbl_Table.getSelectedRow();
        if (this.vitri == -1) {
            return;
        }
        this.txtMaCD.setText(this.tbl_Table.getValueAt(vitri, 0).toString());
        this.TxtTenCD.setText(this.tbl_Table.getValueAt(vitri, 1).toString());
        this.txtHocPhi.setText(this.tbl_Table.getValueAt(vitri, 2).toString());
        this.txtThoiLuong.setText(this.tbl_Table.getValueAt(vitri, 3).toString());
        this.lbl_tenanh.setText(this.tbl_Table.getValueAt(vitri, 4).toString());
        this.updateHinh(this.tbl_Table.getValueAt(vitri, 4).toString());
        for (ChuyenDe cd : lstCD) {
            if (this.txtMaCD.getText().equals(cd.getMaCD())) {
                this.txtMoTa.setText(cd.getMoTa());
                return;
            }else{
                continue;
            }
        }
    }
    private void btn_firstActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_firstActionPerformed
         try {
            this.vitri = 0;
            this.tbl_Table.setRowSelectionInterval(vitri, vitri);
            this.showAgain();
            this.setStatusVitri();
            this.edit();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btn_firstActionPerformed

    private void btn_backActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_backActionPerformed
        try {
            this.vitri --;
            if (vitri < 0) {
                this.vitri = 0;
            }
            this.tbl_Table.setRowSelectionInterval(vitri, vitri);
            this.showAgain();
            this.setStatusVitri();
            this.edit();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btn_backActionPerformed

    private void btn_nextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_nextActionPerformed
        try {
            this.vitri ++;
            if (vitri > lstCD.size() - 1) {
                this.vitri = lstCD.size() - 1;
            }
            this.tbl_Table.setRowSelectionInterval(vitri, vitri);
            this.showAgain();
            this.setStatusVitri();
            this.edit();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btn_nextActionPerformed

