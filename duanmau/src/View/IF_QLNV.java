    private void getAll(){
        this.model.setRowCount(0);
        this.lstnv = this.NVDAO.getAll();
        for (NhanVien nv : lstnv) {
            Object [] r = new Object[]{
                nv.getMaNV(),
                nv.getMatKhau(),
                nv.getHoTen(),
                nv.setVaiTro()
            };
            this.model.addRow(r);
        }
    }
    
