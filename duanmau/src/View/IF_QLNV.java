    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        if (this.check()) {
            try {
                for (NhanVien nv : lstnv) {
                    if (this.txt_manv.getText().equals(nv.getMaNV())) {
                        JOptionPane.showMessageDialog(this, "Mã nhân viên này đã tồn tại!");
                        return;
                    }
                }
                String manv = this.txt_manv.getText();
                String hoten = this.txt_hoten.getText();
                String password = String.valueOf(this.txt_matkhau.getPassword());
                boolean vaitro;
                if (this.rd_truongphong.isSelected()) {
                    vaitro = true;
                }else{
                    vaitro = false;
                }
                
                NhanVien nv = new NhanVien(manv, password, hoten, vaitro);
                this.NVDAO.insert(nv);
                JOptionPane.showMessageDialog(this, "Thêm thành công!");
                this.getAll();
                this.clear();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btn_themActionPerformed

